const dicts: Record<Lang, Strings> = { en, zh, es };

export function useSleepI18n() {
  const { lang } = useI18n();
  const t = (k: string, vars?: Record<string, string | number>) => {
    const raw = dicts[lang][k] ?? dicts.en[k] ?? k;
    if (!vars) return raw;
    return raw.replace(/\{(\w+)\}/g, (_, key) => String(vars[key] ?? ""));
  };
  return { t, lang };
}